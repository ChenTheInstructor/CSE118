package p2_boolean_values;

public class Main {

	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		boolean c = a && b; // a AND b
		System.out.println(c);
		
		/*	&& AND	
		 * true && true --> true
		 * true && false --> false
		 * false && true --> false
		 * false && false --> false
		 */
	}

}
