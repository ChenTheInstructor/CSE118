package p1;

public class Main {

	public static void main(String[] args) {
		System.out.println("John Doe"); // string
		System.out.println("4.0");// string 
		System.out.println("18"); // string
		System.out.println(4.0 - 0.1); // floating point number
		System.out.println(18 + 3); // integer
		System.out.printf("%-10s%-10.2f%-10d%n","John", 4.0, 18);
		
		//data types
		/*
		 * 1. int: for an integer value
		 * 2. double: for common floating point values
		 * 3. byte: for a small integer value
		 * 4. long: for a very large integer value
		 * 5. float: for a small floating point values
		 * 6. char: for a single character 'a'
		 * 7. boolean: for either true or false
		 * 8: short: for a medium integer value
		 */
		
	}

}
