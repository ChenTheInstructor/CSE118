package p_special_characters;

public class DemoSpacialCharacters {

	public static void main(String[] args) {
		System.out.println("10\\n\\n\\n20"); // \n: line breaker
		System.out.println("10 \\t 20"); // \t tab
		System.out.println("10\\t\\t\\t20"); // \t tab
		
		// \: escape character used to neutralize the special character
		}

}
