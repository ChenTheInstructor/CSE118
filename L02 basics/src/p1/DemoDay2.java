package p1;

public class DemoDay2 { // class header
	public static void main(String[] args) { // main method is where a java program starts
		// input (of data) 
		int radius1 = 5;
		double radius2 = 5.5;
		
		// process (data)
		double area1 = Math.PI * radius1 * radius1;
		double area2 = Math.PI * radius2 * radius2;
		
		
		// output (of result)
		System.out.println(area1);
		System.out.println(area2);
	}
}
