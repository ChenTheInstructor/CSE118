package p1;

public class Student {
	// fields
	private String name;
	private double gpa;
	
	// methods
	public void setName(String aName) {
		name = aName;
	}
	
	public void setGpa(double aGpa) {
		gpa = aGpa;
	}
	
	
	public String getName() {
		return name;
	}
	
	public double getGpa() {
		return gpa;
	}

}
